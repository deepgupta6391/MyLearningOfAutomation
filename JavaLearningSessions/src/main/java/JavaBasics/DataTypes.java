public class DataTypes {

	public static void main(String[] args) {
		//primivitie data types: byte,short,int,long,double,float,char,boolean
		
		// data types:
		// numeric data types:
		// byte,short,int,long--Integer Family

		// size: 1 byte: 8 bits
		// range:-128 to 127
		byte b = 1;
		b = -128;
		
		//short:
		// size: 2 bytes
		// range:-32768 to 32767
		short s = 20;
		short s1=-32768;
		short s3=32767;
	
		//int:
		//size: 4 bytes: 32 bits
		//range:
		int i=10;
		
		//long:
		//size: 8 bytes: 64 bits
		//range:-2^63 to 2^63-1
		long l=1299982;//(ex:population, distance between earth and moon, blood count, hairs)
		
		
		//Double family
		
		//float: 4 bytes: 32 bits
		//range: after point it can take upto 7 digits
		float f1=-12.34f;
		float f2=(float) 12.34;
		
		//double:
		//size: 8 bytes: 64 bits
		//range: it can take upto 15 digits after point 
		double d=12.33;
		double d1=-78.00;
		
		
		
		//char:only single digit value
		//size:2 bytes
		char c1='1';
		char c2='$';
		
		//boolean:~1 bit(actually less than 1 bit)
		boolean b11=true;
		boolean b12=false;
		
		//String:
		String st="Hellos";
		
				
		
		
	}

}
