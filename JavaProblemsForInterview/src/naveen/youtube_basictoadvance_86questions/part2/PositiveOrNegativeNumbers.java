package naveen.youtube_basictoadvance_86questions.part2;

public class PositiveOrNegativeNumbers {

	public static void main(String[] args) {

		double number = -45.98;
		if (number < 0.0) {
			System.out.println(number + " its a negative number");
		} else if (number > 0.0) {
			System.out.println(number + " its a positive number");
		} else {
			System.out.println(number + " is zero");
		}

	}

}
